
public class OOP {

	public int num1;
	public int num2;
	

	OOP(int num1, int num2){
		this.num1 = num1;
		this.num2 = num2;
	}
	public int addition() {
		return this.num1 + this.num2;
	}
	public int subtraction() {
		return this.num1 - this.num2;
	}
	public int division() {
		return this.num1 / this.num2;
	}
	public int multiplication() {
		return this.num1 * this.num2;
	}
}
 