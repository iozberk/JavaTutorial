
public class ForLoops {

	public static void main(String[] args) {
		
		
		for(int i = 1; i <= 5; i++) {
			System.out.println(i);
		}
		System.out.println("-------------------------");
		for(int i = 0; i <= 10; i = i + 2) {
			System.out.println(i);
		}
		System.out.println("-------------------------");
		for(int i = 0; i <= 20; i = i + 3) {
			System.out.println(i);
		}
		
		System.out.println("-------------------------");
		for(int i = 20; i >= 0; i = i - 1) {
			System.out.println(i);
		}
	}
	

}
